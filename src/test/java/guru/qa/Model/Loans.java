package guru.qa.Model;

public class Loans {
    private String id;
    private String docNumber;
    private String type;
    private String creditHistoryType;
    private String currencyCode;

    public String getId() {
        return id;
    }

    public String getDocNumber() {
        return docNumber;
    }

    public String getType() {
        return type;
    }

    public String getCreditHistoryType() {
        return creditHistoryType;
    }

    public String getCurrencyCode() {
        return currencyCode;
    }
}
